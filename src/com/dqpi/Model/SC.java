package com.dqpi.Model;

public class SC {
	private String Sno;
	private int SCg;
	private String Cno;
	public String getSno() {
		return Sno;
	}
	public void setSno(String sno) {
		Sno = sno;
	}
	public int getSCg() {
		return SCg;
	}
	public void setSCg(int sCg) {
		SCg = sCg;
	}
	public String getCno() {
		return Cno;
	}
	public void setCno(String cno) {
		Cno = cno;
	}
}
